    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        aaCliente buscaCliente = ControlClientes.buscaCliente(txtIdentificador.getText());
        
        if(buscaCliente == null){
            lblNombres.setText("");
            lblApellidos.setText("");
            lblGenero.setText("");
            lblEdad.setText("");
            lblDomicilio.setText("");
        }else{
            lblNombres.setText(buscaCliente.getNombre());
            lblApellidos.setText(buscaCliente.getApellido());
            lblGenero.setText(buscaCliente.getGenero());
            lblEdad.setText(String.valueOf(buscaCliente.getEdad()));
            lblDomicilio.setText(buscaCliente.getDomicilio());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        
        try{
            ControlClientes.eliminaCliente(txtIdentificador.getText(), lblNombres.getText(), lblApellidos.getText(), lblGenero.getText(), lblEdad.getText(), lblDomicilio.getText());
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Ingrese una ID de usuario porfavor");
        }
        txtIdentificador.setText("");
        lblNombres.setText("");
        lblApellidos.setText("");
        lblGenero.setText("");
        lblEdad.setText("");
        lblDomicilio.setText("");
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

